#include<bits/stdc++.h>
using namespace std;
const int N=10000+7;

int main()
{
	int T=0;
	cin>>T;
//	int t1=1500*(1-0.03),t2=3000*(1-0.1),t3=4500*(1-0.2),t4=(35000-9000)*(1-0.25),
//	t5=(55000-35000)*(1-0.3),t6=(80000-55000)*(1-0.35);
	
	int t1=1500*0.03,t2=3000*0.1,t3=4500*0.2,t4=(35000-9000)*0.25,
	t5=(55000-35000)*0.3,t6=(80000-55000)*0.35;
	
	int ans=0;

	if(T<=3500)	ans=0;
	else if(T+t1<=3500+1500)	ans=(T-3500)/(1-0.03)*0.03;
	else if(T+t1+t2<=3500+4500)	ans=t1+(T+t1-3500-1500)/(1-0.1)*0.1;
	else if(T+t1+t2+t3<=3500+9000)	ans=t1+t2+(T+t1+t2-3500-4500)/(1-0.2)*0.2;
	else if(T+t1+t2+t3+t4<=3500+35000)	ans=t1+t2+t3+(T+t1+t2+t3-3500-9000)/(1-0.25)*0.25;
	else if(T+t1+t2+t3+t4+t5<=3500+55000)	ans=t1+t2+t3+t4+(T+t1+t2+t3+t4-3500-35000)/(1-0.3)*0.3;
	else if(T+t1+t2+t3+t4+t5+t6<=3500+80000)	ans=t1+t2+t3+t4+t5+(T+t1+t2+t3+t4+t5-3500-55000)/(1-0.35)*0.35;
	else ans=t1+t2+t3+t4+t5+t6+(T+t1+t2+t3+t4+t5+t6-3500-80000)/(1-0.45)*0.45;

	cout<<ans+T<<endl;
	return 0;
}
